
public class swapping2nos {

	public static void main(String[] args) {
		// Swapping 2 nos
		
		float a = 2.5f;
		float b = 3.5f;
		System.out.println("Before swap");
		System.out.println("a="+a);
		System.out.println("b="+b);
		
		float temp= a ;
		a = b;
		b = temp;
		
		System.out.println("After Swap");
		System.out.println("a="+a);
		System.out.println("b="+b);
		
		
		
		
		
		
		

	}

}
